public class Market {
    String Name;
    int Price;
    String Used;
    String Image;

    public String getImage() {
        return Image;
    }

    public void setImage(String Image) {
        this.Image = Image;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public int getPrice() {
        return Price;
    }

    public void setPrice(int Price) {
        this.Price = Price;
    }

    public String getUsed() {
        return Used;
    }

    public void setUsed(String Used) {
        this.Used = Used;
    }
    
    
}
